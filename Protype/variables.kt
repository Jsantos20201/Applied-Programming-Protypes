fun main() {
    // Integer types
    val byteVar: Byte = 10
    val shortVar: Short = 100
    val intVar: Int = 1000
    val longVar: Long = 100000L

    // Floating-point types
    val floatVar: Float = 3.14f
    val doubleVar: Double = 3.14159

    // Boolean type
    val boolVar: Boolean = true

    // Character type
    val charVar: Char = 'A'

    // String type
    val stringVar: String = "Hello, Kotlin!"

    // Output
    println("Byte Variable: $byteVar")
    println("Short Variable: $shortVar")
    println("Int Variable: $intVar")
    println("Long Variable: $longVar")
    println("Float Variable: $floatVar")
    println("Double Variable: $doubleVar")
    println("Boolean Variable: $boolVar")
    println("Character Variable: $charVar")
    println("String Variable: $stringVar")
}

# Variable demonstration program in R

# Assigning a value to a variable
message <- "Hello, R!"

# Printing the value of the variable
print(message)

# Updating the value of the variable
message <- "Updated message!"

# Printing the updated value
print(message)

# Mathematical operations using variables
x <- 5
y <- 3

# Addition
sum <- x + y
print("Addition:")
print(sum)

# Subtraction
diff <- x - y
print("Subtraction:")
print(diff)

# Multiplication
product <- x * y
print("Multiplication:")
print(product)

# Division
quotient <- x / y

# Printing the division result
print("Division:")
print(quotient)
